    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
        String nombre = nameTxt.getText();
        String apP = apPTxt.getText();
       

        // Validaciones para los campos
        if (nombre.isEmpty() || apP.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            nameTxt.requestFocus();
            return;
        }

        com.chocolateTienda.models.producto productos = isEdition ? userEdition : new com.chocolateTienda.models.producto();
        productos.setNombre(nombre);
        int precio = Integer.parseInt(apP);
        productos.setPrecio(precio);
       
        try {
            DAOproducto dao = new DAOproductoImpl();
            if (!isEdition) {
                dao.registrar(productos);
            } else {
                dao.modificar(productos);
            }

            String successMsg = isEdition ? "modificado" : "registrado";

            javax.swing.JOptionPane.showMessageDialog(this, "Producto " + successMsg + " exitosamente.\n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
            if (!isEdition) {
                nameTxt.setText("");
                apPTxt.setText("");
            }
        } catch (Exception e) {
            String errorMsg = isEdition ? "modificar" : "registrar";
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurri√≥ un error al " + errorMsg + " el Producto. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
  
    }//GEN-LAST:event_buttonActionPerformed

